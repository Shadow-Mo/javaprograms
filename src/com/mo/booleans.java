package com.mo;

public class booleans {
    public static void main(String[] args) {
//        boolean values
        boolean javafun = true;
        boolean fishtasty = false;
        System.out.println(javafun);    //outputs true
        System.out.println(fishtasty);  //outputs false

//        boolean expression
        int x = 10;
        int y = 9;
        System.out.println(x > y);     //returns true

        int x1 = 10;
        System.out.println(x1==10);    //returns true
        System.out.println(10==15);    //returns false
    }
}
